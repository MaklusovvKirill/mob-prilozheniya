try:
    age = int(input("Введите ваш возраст: "))
    if age < 0:
        print("❌ Ошибка: возраст не может быть отрицательным!")
    elif age <= 12:
        print("✅ Ваша категория: Ребенок")
    elif age <= 17:
        print("✅ Ваша категория: Подросток")
    elif age <= 64:
        print("✅ Ваша категория: Взрослый")
    else:
        print("✅ Ваша категория: Пенсионер")
except ValueError:
    print("❌ Ошибка: введите корректное целое число!")
